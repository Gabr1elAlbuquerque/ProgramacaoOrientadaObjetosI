
package a02ex04;

public class A02ex04 {

    public static void main(String[] args) {
        int x = 10, y = 3;
        System.out.println("X = "+x);
        System.out.println("Y = "+y);
        System.out.println("-X = "+(-x));
        System.out.println("-Y = "+(-y));
        System.out.println("X/Y = "+(x/y));
        System.out.println("Resto de X por Y = "+(x%y));
        System.out.println("Icremento de X = "+(++x));
    }
    
}
